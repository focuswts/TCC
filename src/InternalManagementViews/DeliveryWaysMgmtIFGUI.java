    private void fill_tbDeliveryW() {

        DefaultTableModel aModel = (DefaultTableModel) tb_DeliveryW.getModel();
        try {
            FormasEntregaDAO dwDAO = new FormasEntregaDAO();
            ArrayList<FormasEntrega> Formas = null;
            Formas = dwDAO.findAll();

            int numLinhas = aModel.getRowCount();

            for (int i = 0; i < numLinhas; i++) {
                aModel.removeRow(0);
            }

            for (int i = 0; i < Formas.size(); i++) {
                Object[] rowDados = new Object[5];
                rowDados[0] = Formas.get(i).getForma();
                rowDados[1] = Formas.get(i).getValor();
                aModel.addRow(rowDados);

            }

            this.tb_DeliveryW.setModel(aModel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro Ao Preencher TB Delivery Ways! " + e);
        }

    }

    private FormasEntrega fieldsToOBJ() {
        FormasEntrega dw = new FormasEntrega();
        try {
            String forma = tb_DeliveryW.getValueAt(tb_DeliveryW.getSelectedRow(), 0).toString();
            dw.setForma(forma);
            String valor = tb_DeliveryW.getValueAt(tb_DeliveryW.getSelectedRow(), 1).toString();
            dw.setValor(valor.substring(2,valor.length()));
        } catch (Exception e) {
            System.out.println("Erro Ao Converter Fields To OBJ: " + e);
        }
        return dw;
    }

    private void deleteDeliveryWay() {
        FormasEntrega dw = new FormasEntrega();
        FormasEntregaDAO dwDAO;
        dwDAO = new FormasEntregaDAO();
        String forma = tb_DeliveryW.getValueAt(tb_DeliveryW.getSelectedRow(), 0).toString();
        dw = dwDAO.getOBJFromForma(forma);
        try {
            if (dwDAO.delete(dw) == true) {
                JOptionPane.showMessageDialog(this, "Forma De Entrega ExcluÃ­da Com Sucesso!");
            }
            fill_tbDeliveryW();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro Ao Deletar Forma De Entrega: " + e);
        }

    }

    private void viewOpsDeliveryS(String operacao) {
        DeliveryWaysOpsIFGUI viewDeliveryW;
        try {
            if (operacao.equals("Add")) {
                viewDeliveryW = new DeliveryWaysOpsIFGUI(null, true, operacao);
                this.getParent().add(viewDeliveryW);
                viewDeliveryW.toFront();
                viewDeliveryW.setVisible(true);
            }
            if (operacao.equals("Edit")) {
                viewDeliveryW = new DeliveryWaysOpsIFGUI(null, true, operacao, fieldsToOBJ());
                this.getParent().add(viewDeliveryW);
                viewDeliveryW.toFront();
                viewDeliveryW.setVisible(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro Ao Abrir View Ops Models: " + e);
        }

    }

