    private void fill_tbModels() {

        DefaultTableModel aModel = (DefaultTableModel) tb_Models.getModel();
        try {
            ModelosDAO mDAO = new ModelosDAO();
            ArrayList<Modelos> Modelos = null;
            Modelos = mDAO.findAll();

            int numLinhas = aModel.getRowCount();
            for (int i = 0; i < numLinhas; i++) {
                aModel.removeRow(0);
            }
            for (int i = 0; i < Modelos.size(); i++) {
                Object[] rowDados = new Object[2];
                rowDados[0] = Modelos.get(i).getIdMarca().getMarca();
                rowDados[1] = Modelos.get(i).getModelo();
                aModel.addRow(rowDados);
            }
            this.tb_Models.setModel(aModel);
        } catch (Exception e) {
            System.out.println("Erro Ao Preencher JTable: " + e);
        }

    }

    private void deleteModel() {
        Modelos m = new Modelos();
        ModelosDAO mDAO;
        mDAO = new ModelosDAO();
        int idModel = mDAO.getIdModelo(tb_Models.getValueAt(tb_Models.getSelectedRow(), 1).toString());
        try {
            m.setId(idModel);
            if (mDAO.delete(m) == true) {
                JOptionPane.showMessageDialog(this, "Modelo ExcluÃ­do Com Sucesso!");
            }
            fill_tbModels();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro Ao Deletar Modelo: " + e);
        }

    }

    private Modelos fieldsToOBJ() {
        Modelos m = new Modelos();
        ModelosDAO mDAO = new ModelosDAO();
        try {
            String modelo = tb_Models.getValueAt(tb_Models.getSelectedRow(), 1).toString();
            int idModelo = mDAO.getIdModelo(modelo);
            m.setId(idModelo);
            m.setModelo(modelo);
            System.out.println(m.getModelo());
            return m;
        } catch (Exception e) {
            System.out.println("Erro AO Gerar OBJ: " + e);
            return null;
        }

    }

    private void viewOpsModels(String operacao) {
        ModelsOpsIFGUI viewModels;
        try {
            if (operacao.equals("Add")) {
                viewModels = new ModelsOpsIFGUI(null, true, operacao);
                this.getParent().add(viewModels);
                viewModels.toFront();
                viewModels.setVisible(true);
            }
            if (operacao.equals("Edit")) {
                viewModels = new ModelsOpsIFGUI(null, true, operacao, fieldsToOBJ());
                this.getParent().add(viewModels);
                viewModels.toFront();
                viewModels.setVisible(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro Ao Abrir View Ops Models: " + e);
        }

    }

